class Solution {
public:
    int InversePairs(vector<int> data) {
        if (data.size() <= 1) {
            return 0;
        }

        vector<int> space(data.size());

        int res = inverse_pairs_core(data, space, 0, data.size() - 1) % 1000000007;

        // for (vector<int>::iterator it = data.begin(); it != data.end(); it++) {
        //     cout << *it << " ";
        // }
        // cout << endl;

        return res;
    }

private:
    // 得要使用long long类型返回逆序对数，否则在牛客网上通不过，晕！
    long long inverse_pairs_core(vector<int>& data, vector<int>& space, int l, int u) {
        if (l == u) {
            return 0;
        }

        int m = l + ((u - l) >> 1);
        long long lcount = inverse_pairs_core(data, space, l, m);
        long long rcount = inverse_pairs_core(data, space, m + 1, u);
        int p = m;  // left part last item index
        int q = u;  // right part last item index
        int index = u;
        long long res = 0;
        while (p >= l && q >= m + 1) {
            if (data[p] > data[q]) {
                space[index--] = data[p--];
                res += q - m;
            } else {
                space[index--] = data[q--];
            }
        }
        for (; p >= l; p--) {
            space[index--] = data[p];
        }
        for (; q >= m + 1; q--) {
            space[index--] = data[q];
        }
        for (int i = l; i <= u; i++) {
            data[i] = space[i];
        }

        return lcount + rcount + res;
    }
};

